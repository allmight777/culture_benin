<script>
// Validation côté client
(function() {
  'use strict';
  window.addEventListener('load', function() {
    var forms = document.getElementsByClassName('needs-validation');
    var validation = Array.prototype.filter.call(forms, function(form) {
      form.addEventListener('submit', function(event) {
        if (form.checkValidity() === false) {
          event.preventDefault();
          event.stopPropagation();
        }
        form.classList.add('was-validated');
      }, false);
    });
  }, false);
})();

// Gestion de l'upload de fichier
document.addEventListener('DOMContentLoaded', function() {
  const fileInput = document.getElementById('fichier');
  
  fileInput.addEventListener('change', function(e) {
    if (this.files && this.files[0]) {
      const fileName = this.files[0].name;
      const fileSize = (this.files[0].size / 1024).toFixed(2);
      
      // Auto-remplir certains champs
      const cheminInput = document.getElementById('chemin');
      if (cheminInput && !cheminInput.value) {
        cheminInput.value = `/uploads/${fileName}`;
      }
      
      const typeInput = document.getElementById('type_media');
      if (typeInput) {
        const ext = fileName.split('.').pop().toLowerCase();
        if (['jpg', 'jpeg', 'png', 'gif', 'webp'].includes(ext)) {
          typeInput.value = 'image';
        } else if (['mp4', 'avi', 'mov', 'wmv'].includes(ext)) {
          typeInput.value = 'video';
        } else if (['mp3', 'wav', 'ogg'].includes(ext)) {
          typeInput.value = 'audio';
        } else if (['pdf', 'doc', 'docx', 'txt'].includes(ext)) {
          typeInput.value = 'document';
        }
      }
      
      const tailleInput = document.getElementById('taille');
      if (tailleInput) {
        tailleInput.value = Math.round(this.files[0].size / 1024);
      }
    }
  });
});
</script>

